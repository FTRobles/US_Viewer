    def changeAxis(self, tag):  
        # This condition checks for the axis selected
        if (tag == "X"):
            # Rotate the view selected and saves the value of the slicer
            if(self.viewSelected == 0):
                self.slicerSagital.rotate(self.horizontalSliderX.value(), X)
                self.valueSlidersSagital[0] =  self.horizontalSliderX.value()
            elif(self.viewSelected == 1):
                self.slicerAxial.rotate(self.horizontalSliderX.value(), X)
                self.valueSlidersAxial[0] =  self.horizontalSliderX.value()
            elif(self.viewSelected == 2):    
                self.slicerCoronal.rotate(self.horizontalSliderX.value(), X)
                self.valueSlidersCoronal[0] =  self.horizontalSliderX.value()
        elif (tag == "Y"):
            # Rotate the view selected and saves the value of the slicer
            if(self.viewSelected == 0):
                self.slicerSagital.rotate(self.horizontalSliderY.value(), Y) 
                self.valueSlidersSagital[1] =  self.horizontalSliderY.value()
            elif(self.viewSelected == 1):
                self.slicerAxial.rotate(self.horizontalSliderY.value(), Y)
                self.valueSlidersAxial[1] =  self.horizontalSliderX.value()
            elif(self.viewSelected == 2):    
                self.slicerCoronal.rotate(self.horizontalSliderY.value(), Y)
                self.valueSlidersCoronal[1] =  self.horizontalSliderY.value()
        elif (tag == "Z"):
            # Rotate the view selected and saves the value of the slicer
            if(self.viewSelected == 0):
                self.slicerSagital.rotate(self.horizontalSliderZ.value(), Z)
                self.valueSlidersSagital[2] =  self.horizontalSliderZ.value()
            elif(self.viewSelected == 1):
                self.slicerAxial.rotate(self.horizontalSliderZ.value(), Z)
                self.valueSlidersAxial[2] =  self.horizontalSliderZ.value()
            elif(self.viewSelected == 2):    
                self.slicerCoronal.rotate(self.horizontalSliderZ.value(), Z)
                self.valueSlidersCoronal[2] =  self.horizontalSliderZ.value()

        # The function updateView() is called to update the three views    
        self.updateView()
        
        return

    def updateView(self):
        #The three new views are obtained (displaced or rotated) and set to the viewer

        rotatedSliceSagital = self.slicerSagital.getOutput()
        self.viewSagital.setSlice(rotatedSliceSagital)
        
        rotatedSliceAxial = self.slicerAxial.getOutput()
        self.viewAxial.setSlice(rotatedSliceAxial)
        
        rotatedSliceCoronal = self.slicerCoronal.getOutput()
        self.viewCoronal.setSlice(rotatedSliceCoronal)
        
        return


